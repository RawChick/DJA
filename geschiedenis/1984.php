<h3>Algemeen </h3>

Enkele extra vergaderingen werden gewijd aan de ontwikkelingen rond het door het bestuur beëindigde trainerschap van W. v. Rijn (miv 19.06.1984).<br> 
Hieraan werd een extra algemene ledenvergadering gewijd op 14.08.1984, <br>
waar het zittende bestuur als tevoren aangekondigd in zijn geheel zou aftreden. <br>
Bij gebrek aan voldoende vervangers werd dit uitgesteld tot de volgende algemene ledenvergadering van 28.02.1985.<br> 
Twee nieuwe mensen (A. van Baal en Diana Hoekman) draaiden sinds 14.08.1984 reeds mee in het nog zittende bestuur.<br>
J. Hereijgers maakt laatste jaarverslag als secretaris en wordt opgevolgd door Diana van Bergen.  <br>
<br>
Dit alles maakte 1984 tot een erg ROERIG JAAR in de DJA-geschiedenis. <br>
<br>Het bestuur droeg de volgende leden voor als sportman/vrouw: John van Sprundel (B-Jun.), <br>
Willian Vissers (C-Jun.), Herman Krijnen (Sen.), Nancy Roovers (A-Jun.), Evie Roelands (Sen.) en Jolanda Dictus (B-Jun.). <br>
Op de algemene ledenvergadering werden Evie Roelands en John van Sprundel als zodanig gekozen.<br>
Nieuw voorstel sportverkiezingen: belangrijke prestaties tijdens TC vergaderingen vastleggen. <br>
Einde van het seizoen doet de TC voordracht naar bestuur en deze beslist.<br>
M. Jansen en F. Hereijgers volgden een bestuur kadercursus.<br>
Men slaagde erin om steeds meer met de bus naar wedstrijden te gaan, de algemene indruk was, dat dit als gezellig werd ervaren.<br>
Er wordt gewerkt aan een reglement waarin de tuchtrechtspraak binnen DJA wordt geregeld.<br>
<br>De oud-papieractie (RBT+Zundert-Zuid) werd wederom een financieel succes.<br>
Grote Clubactie: 800 loten. <br>
Totaal aantal leden aan het eind van het jaar bedroeg: 203 leden. Ledental daalde met 4 in totaal.<br>
Totale verloop van dit jaar is 39.<br> 
<br>
  

<h3>Trainingen </h3>

Het “gedwongen” vertrek van trainer W. v. Rijn leidde zoals vermeld tot spanningen binnen DJA.<br>
Ook Evie Roelands stopte met het geven van trainingen. Al met al ontbrak eind 1984  een looptrainer.<br>
Positief was dat de samenwerking tussen trainers en recreatie-sportleiders leidde tot een wederom zeer geslaagde avonturentocht.<br>
Ron Schijfs en Berlinde Verhagen slaagden voor JAL.<br>
<br>
<h3>RSC </h3>

Uit de RSC gingen dat jaar: H. v. Hassel, C. Konings en A. v. Aert. <br>
Men verwelkomde: A. v. Baal en J. Mathijssen. J. Zagers maakte ook nog deel uit van de TC.<br>
In dit jaar bedroeg het aantal recreatieleden 110 , waarvan 93 vrouwen en 17 mannen.<br>
Door NKS werden een aantal ondersteunings-lessen verzorgd voor de RS-leiders.<br>
<br>Het aantal evenementen dat de RSC organiseerde was weer zeer divers:<br>
Een rikconcours met zeer geringe belangstelling. De feestdag DJA: broodmaaltijd met soep en disco. <br> 
Men nam deel aan de sportmanifestatie georganiseerd door de Sportstichting. Fien Hereijgers gaf hier  een demonstratie.<br>
Men stelde nieuwe regels op om lid te worden: men  mag 5 keer proefdraaien. Daarna keuze uit een   7-tal recreatiesporturen.<br>
Trimuur op zondagmorgen is komen te vervallen.<br>
RBT-veiling was niet meer beschikbaar, zodoende ging de woensdagavond groep naar het eigen DJA-terrein.<br>
De bingo-avond ontbrak ook niet en met 132 deelnemers wederom een succes.<br>
De deelname aan de vierdaagse liep terug naar 365 deelnemers, men was toch wel teleurgesteld.<br>
Nieuwe  ideeën werden geopperd om daar nieuw leven in te brengen. <br>
Men dacht o.a om de start te verplaatsen naar de accommodatie en het eindpunt naar het Oranjeplein te brengen.<br> 
Verder sprak men erover om de vierdaagse over 5 dagen uit te smeren, met de bepaling dat men verplicht was vier dagen uit te lopen.<br>
Tot slot ging de RSC langlaufen in de Ardennen. <br>
<br>

