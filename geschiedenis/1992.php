<h3>Algemeen </h3>
Weer een bijzonder jaar voor DJA met de opening van de huidige kantine. <br>
Een schitterende collage vereeuwigde de tot stand koming onder leiding van onze opperman Gerrit Hoekman.<br>
Jac en Thea van Nispen werden de beheerders. <br>
Vanaf 1 april is de kantine iedere woensdagavond open vanaf 19.30 uur. <br>
De gemeente Zundert kreeg een prijs van de KNAU  voor de tot standkoming van de nieuwe baan.<br>
Sponsorloop van 11 jul 1992 olv Jan Hereijgers, Jan Schrauwen en Diana van Bergen werd gehouden om het clubgebouw te financieren. <br>
Deze loop werd gecombineerd met een familiedag. Doel: DJA wordt één die dag. Ieder mag met heel zijn gezin deelnemen. <br>
Het  streefbedrag: fl. 15000,= werd niet gehaald maar het bedrag van 9000 gulden was toch niet te versmaden.<br>
Oud papier verzameld door  G. Hoekman en Jac Verheyen. Jac Verheyen stopt eind 1992  hiermee.<br>
Grote Clubactie en actie Gehandicapten Sport vonden ook nog plaats maar in 1993 werd gestopt met de Gehandicapten actie.<br>
Samenstelling van het bestuur: voorz. (voorlopig niemand), Diana v.Bergen (secr), Elly de Labie (penn.), Hans de Wijs, <br>
Diana Verhagen, Ed Jacobs, Jan van Hees, Jolanda Dictus (vanaf april 1992), bestuursleden en met ingang van januari 1993 Pieter Legius.<br>
Sportman en sportvrouw 1992: Alfons van Geel en Heidy Gommers<br>
Begin 1992: 322 leden. (atletiek: 169, 37extra in 1991. Recreatie:153)<br>
    <br>
<h3>Trainingen</h3> 
Trainingen verlopen prima. Loopgroepen groeien weer uit hun jasje. <br>
In okt. 1992 stopt Jac van Nispen. Hij gaat samen met Thea de kantine doen. Jac is toch aangebleven en neemt 20 atleten voor zijn rekening. <br>
Juul Rayee 30 en Jan Dictus is aangetrokken voor de overige 30 atleten (Jan geeft alleen training als Jules er niet is).<br>
Jeanne van den Berg wordt voor 3 maanden vervangen door Edwin Verhoeven, alleen op zaterdag  (CD-jun.meisjes t/m april), <br>
later ook hulp van Erik de Hoon, omdat groep CD-jun.meisjes hard groeit.<br>
Erik combineert het met de Pupillen. Nicole Verhoeven is bij de Pupillen toegevoegd als trainster.<br>
Pupillen trainsters worden nu bijgestaan door Heidy Gommers, Linda Elst en Erik de Hoon.<br>
Streven om in april 1992 te starten met trimgroep op zondag van 9-10 uur. Dit was olv Gerrit Hoekman een succes te noemen (ongeveer 50 leden).<br>
De donderdagavond loopgroep heeft een andere trainer gekregen: Johan van Aert. Ria van Kempen is gestopt met de avond-training. <br>
Zij begeleidt wel de dinsdagmiddag-wandelgroep en geeft training op woensdagochtend samen met Ans Kerstens. Later stopt Ria en neemt Jose Lambregts-Ooms het over.<br>
Wandelgroep dinsdagmiddag ongeveer 20 leden.    <br>
Jeanne van den Berg en Jules Rayee zijn resp. bezig met AT- cursus en trainer coach-B.<br>
  <br>
<h3>RSC </h3>
Samenstelling: Diana Verhagen (voorz,best.), Nel Bosma, Ria van Kempen, Ans Kerstens (RSL), Marij Brouwers, <br>
Diana van Opdorp en Karien Oostvogels (leden). Ria van Kempen stopt miv 1993, Paula van Erk is het nieuwe lid.<br>
De coopertesten trekken gemiddeld 22 deelnemers per keer. <br>
De vierdaagse startte dit jaar van de accommodatie en trok 300 deelnemers.<br>
Het was opvallend dat de herfstwandeltocht een grote teruggang zag in deelnemers.<br>
Daarentegen was het aantal deelnemers aan de avonturentocht erg hoog: 400 deelnemers.<br>
  <br>
<h3>Atleten</h3> 
Begin 1992 bestond de TC nog steeds uit 10 personen en waren wel enige wisselingen te noteren:  <br>
Begin 1992 treden af: Elly Jochems (overgenomen door Willio vd Broek, wedstr.secr. thuis) en Gerrit Hoekman<br>
In april 1992 is Thea van Nispen gestopt, overgenomen door Karel Wamsteker.<br>
Naast de bijna als vaste wedstrijden op de kalender, werd een avondwedstrijd estafette georganiseerd.<br>
Doordat de toestemming om deze wedstrijd door te laten gaan pas enige weken voor  <br>
de wedstrijd datum binnenkwam en ook de datum wat ongelukkig viel waren er weinig deelnemende ploegen (alleen maar DJA-teams). <br>
In totaal organiseerde de TC 12 wedstrijden.<br>
De 10 EM werd als traditioneel op Pinksterzaterdag georganiseerd en trok 200 deelnemers, waaronder 15 vrouwen. <br>
  
