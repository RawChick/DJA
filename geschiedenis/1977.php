<h3>Atleten</h3>

De wens om meer commissies samen te stellen kreeg gestalte in de oprichting van de TC (Technische Commissie).<br>
Deze startte  na de jaarvergadering met: J. Boomaars, J. Ooms, J. Schrauwen, G. Hoekman en A. Hereijgers. <br>
In september kwamen daarbij: L.de Hoon en Wil van Uithoven.<br>
Een herenteam bestaande uit: Jac Ooms, L. de Hoon, J. Hereijgers, Cees Vissers, G. Hoekman, F. de Hoon en J. Schrauwen nam deel aan <br>
de competitie 3e klas en werd daar 7e van de 17 ploegen. <br>
Ronald Vreysen en Ron Schijfs kregen een uitnodiging om deel te nemen aan een internationale wedstrijd met deelnemers uit Duitsland,  België en Frankrijk. <br>
Ronald werd 9e op de 1000 meter en Ron 3e op de hinkstapsprong respectievelijk 2.40.5 min en 13.24 meter.<br>
