<p>We mogen stellen dat 1978 een bijzonder jaar was voor DJA. In dat jaar (op 7 mei) kreeg <br>
	DJA haar eigen accommodatie aan de  Klein Zundertseweg. Een gravel atletiekbaan van 285 meter,<br> 
	met een hoogspring aanloop die bestond uit kunststof tegels. <br>
	Ook ontbrak een waterbak niet in de baan die voor de later te noemen steeple chase gebruikt zou kunnen worden.<br>
Twee kleedkamers completeerden het geheel. Inderdaad geen kantine en geen inrichting voor de kleedkamers. <br>
Een hoogspringvalmat behoorde ook niet tot de uitrusting. <br>
In de clubbladen werden dan ook oproepen gedaan om geen “spullen” naar het grof vuil te brengen maar eerst aan DJA te denken. <br><br>
De grote animator in de realisering mag toch wel A. Kimmel worden genoemd. <br>
Maar ook het voltallige bestuur droeg daar meer dan een steentje aan bij, de samenstelling was:<br>
Voorz. Kimmel, nu formeel tot vz gekozen, secr./penn. A. Hereijgers, leden Mevr. A. Rens, J.van Aert, G. Hoekman, A. Michielsen en C. Vissers. (totaal 7 leden)<br><br>
Veder nog wedstr. secr. J. Boomaars, adm.ledencontr. Dhr. A.vd Borst,  fin.boekhouding J. Hereijgers (oud bestuurslid) geeft hulp aan secr. Met dubbelfunctie.<br>
WBAC + publiciteit: G.Hoekman.<br>
<br>
 

Naast dit heuglijke feit was 1978 op prestatiegebied een uniek jaar te noemen.<br>
RON SCHIJFS: Nederlands Indoorkampioen B-Jun. 1978 h.s.s. 13.38 m.<br>
NK 4, 5, 6 aug. 1978: W.van Uythoven kogelslingeren 54.20 meter.<br>
Zuid.Ned. Meerkampkampioenschappen, Roermond, 12-13 aug. Peter Frijters 3e met 4146 pt. André Frijters 4e met 3738 pt.<br>
Interland in Duitsland 23-24 sept. André Frijters, op uitnodiging van de  KNAU, werd derde bij het verspringen 6.03 m.<br>
Landelijke ranglijst: 23. Ron Schijfs verspringen als junior A, bij het h.s.s. 1e en dat was ook een 5e plaats bij de senioren. <br>
Bij het kogelslingeren nam W. v. Uythoven toen voor even een 1e plaats in.<br><br>
Verder nog de prestaties van Louis en Frank de Hoon die records vestigden die nog steeds niet verbeterd zijn. <br>
Wat te denken bijv. van het uurrecord 18.114 meter van Louis de Hoon. Deze kwam ook op de WBAC record lijst.<br>
Zie ook verder op in dit verslag onder Recordscommissie.<br><br>
Op voorstel van Diana Hoekman (nu van Bergen) werd dit jaar ook een verkiezing van de sportvouw van het jaar ingesteld.<br>
Aantal leden: 84 atletiekà92 134 trimleden.à112 men zag de terugloop van de trimmers veroorzaakt doordat de sporthal Zundert <br>
in gebruik werd genomen met een groot aanbod van indoor sporten.<br>


<h3>Trainingen</h3>

De beschikbare trainers waren: W. v. Uythoven, G. Hoekman, Johan en Annie van Aert, J. Hereijgers en J. Schrauwen. <br>
Vanaf  7 mei werd er natuurlijk getraind op de nieuwe accommodatie.<br>
De trimmers gingen van de dinsdagochtend naar de woensdagochtend.<br>
De jeugd ging op de fiets naar de Tiggeltseberg en trainde 1 uur in de Pannenhoef.<br>
Fien Hereijgers-Baelemans en Johan van Aert namen deel aan de cursus Receatie Sportleider<br>
Duur: 70 lesuren waaronder enkele weekenden, uiteraard afgesloten met een examen.<br>

 

<h3>Trimclub</h3>

De trimcommissie wordt RSC (Recreatie Sport Commissie) en bestaat uit: Fons van Dijck (vz), Ria van Beek (secr), <br>
leden: J. Zagers, F. Herijgers, J.vd Zanden, Corry Konings,<br>
Anneke Hombergen-Rens, A. vanAert, J. van Aert, A. Michielsen.<br>
Met handhaving van de vaste elementen zagen we dit ook nog:<br><br>
<ul>
<li>autopuzzelrit op hemelvaartsdag</li>
<li>feestdag met o.a. voetbalwedstrijd</li>
<li>deelname Fortuna fiets-vierdaagse</li>
<li>deelname AVRO-cross</li>
<li>coopertests en circuittrainingen</li>
<li>diverse triminstuifs (open avonden)</li>
<li>een balavond met levende muziek behoorde ook tot het lijstje van evenementen</li>
</ul><br>
1 oktober 1978 is de aktie “Loop Mee” van start gegaan, om trimmen in de belangstelling te brengen.<br>

 

<h3>Jeugd</h3>

Jeugdmiddag 1 april.<br>
Bij het WBAC cross klassement behaalden de pupillen een 3e plaats in het clubklassement. <br>
De junioren waren iets minder goed met een 5e plaats.<br>

 

<h3>Atleten</h3>

Zoals al eerder vermeld waren de atleten enorm op dreef. DJA was dan ook bij 81 wedstrijden aanwezig met in totaal van 697 deelnemers op 1297 nummers.<br> 
Naast de crossen ging ook weer de 10 EM wedstrijd van start, ditmaal georganiseerd door de TC. <br>
Met 74 wedstrijdlopers en 24 prestatielopers kon men van een succes spreken. In de TC nam Maarten Bömer de plaats in van Jan Schrauwen.<br>
Op 19 juli werd een kogelslingerwedstrijd georganiseerd met 4 deelnemers.<br>
DJA had zelfs 2 ploegen die meededen aan de competitie, ditmaal in de 3e en 4e klasse.<br>
Cees Vissers, een van de toenmalige coryfeeën, nam op 23 september afscheid als wedstrijdloper .Hij deed dat bij een vriendenwedstrijd, gehouden in Wernhout.<br>
Sportman van het jaar werd : W. van Uythoven.<br><br>
Corina Monden viel de eer te beurt om als eerste vrouw in de geschiedenis van DJA zich sportvrouw van het jaar te mogen noemen.<br>
Het jury corps kreeg weer 6 leden.<br>
Ron Schijfs werd gekozen tot sportman van het jaar van de gemeente Zundert.<br><br>
Aan het eind van het jaar zagen we de pupil David Bömer opdoemen en meteen in de top eindigen.<br>
Berlinde Verhagen (nu Dekkers) mocht zich bij de categorie B meisjes clubkampioen noemen.<br>
Dezelfde titel was ook voor Corina Monden weggelegd in de categorie C meisjes.<br>

 

<h3>Recordcommissie</h3>

Dit jaar werden 60 aanvragen ingediend, daarbij ook voor de eerste maal indoor records,<br>
P. Frijters op de 40 meter en kogelstoten en Ron Schijfs  op de 60 meter, verspringen en hink stap sprong. <br>
In 1978 gevestigde clubrecords die tot op heden nog niet verbeterd zijn:<br>
Louis de Hoon : 15000 (officieus) 47.41.0 min; 20000m in 1.06.52.6 uur; 1uur 18114 meter en 3000 meter hindernis in 10.30.0 min.<br> 
Ron Schijfs hink stapsprong 14.30 meter, als A junior ook de 100m in 11.5 sec en het hink stap sprong 14.30 m. <br>
Tenslotte voor Ron als C junior de 100 meter in 12.3 sec. Wim van Uythoven kogelstoten 7 ¼ kg 12.76 m.<br>
Bij de categorie C junioren jongens Peter Frijters: 110 m horden 16.3 sec hoogspringen 1.80 m discuswerpen 1kg 33.70 meter. André Frijters: verspringen 6.08 m<br>
Bij de C junioren meisjes Corina Monden: 80 m in 10.8 sec, later wel geëvenaard door Mariska van Koulil en Nina Matthijssen.<br>

 

<h3>Clubblad</h3>

De redactie bestond nu uit A  Hereijgers, J. Rens, J. Hereijgers-Verheyen en F. de Hoon.<br>
G. Hoekman was de man voor de externe publicatie. Daarbij vertegenwoordigde hij DJA bij de ARWB.  <br>
Het clubblad verscheen nu regelmatig en dit jaar zelfs 5 x. Humor en de rubriek “wist u dat” kwamen toen al aan bod, <br>
maar de uitslagen rubriek was toch wel het graag geziene onderdeel.<br>

 

