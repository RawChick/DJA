<h3>Atleten</h3>
De TC kreeg versterking in de persoon van Els de Wijs, die ook de trainers vertegenwoordigde.</br>
Voor het overige bleef de samenstelling als die in 1988.</br>
Een opvallend feit was dat bij de pupillenwedstrijd maar 32 deelnemers waren, in 1988 nog 200!!</br>
De WBAC jeugdcross en de DJA cross, gehouden bij Sauna Diana, vestigden records voor wat betreft het aantal deelnemers.</br>
Respectievelijk 504 en 340, bij deze laatste ook nog eens 80 trimmers.</br>
De gevraagde hulp bij triatlon werd geleverd en bracht een aardige som geld binnen.  </br>
De 10 EM wedstrijd bracht 108 wedstrijddeelnemers en ook nog eens 121 prestatielopers.</br>
