Oud papier: met gemeente Zundert werd een overeenkomst gesloten met “De Kringloper” in Etten-Leur, die het oud papier ging ophalen. </br>
Door de gemeente is een financiële regeling getroffen (afbouwsubsidie) voor die verenigingen die altijd oud papier hebben opgehaald,</br> 
er valt dan toch een flinke financiële post weg. Voor DJA kwam er de laatste jaren tusssen fl.7500,-- en fl.8000,-- netto binnen. </br>
Regeling is 1.10.1994 ingegaan. Daarom in 1994 nog subsidie gekregen (aangevuld), in 1995 nog ruim fl.5000,- en in 1996 nog fl. 3400,--. </br>
Gezien deze financiële aderlating per 01.01.96 contributieverhoging. </br>
DJA had al lang een lagere contributie in vergelijking met de overige atletiek clubs.</br> 
De contributie ging met fl.10,-- p.j. voor Senioren en Recreanten omhoog. Voor jeugd blijft deze gelijk aan 1994. </br>
