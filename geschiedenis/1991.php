<h3>Algemeen </h3>
Samenstelling van het bestuur: Voorz. J. v. Hees, Diana van Bergen secr., penn. Elly de Labie, bestuurslid Hans de Wijs en Ed Jacobs, </br>
Diana Verhagen neemt al vanaf mei 1990 deel aan het bestuur, mevr. A.v. Baal is wegens omstandigheden eerder afgetreden.</br>
Zij hebben in 1991 een hele zware kar getrokken. </br>
Tijdens jaarvergadering van 20.02.1991 legt Hans de Wijs uit: </br>
in april 1990 is de Gemeenteraad tot slotsom gekomen dat er een nieuwe atletiekaccommodatie  moet komen. </br>
Nadat DJA akkoord is gegaan met de verplaatsing naar de Akkermolen, werden door de gemeente gelden beschikbaar gesteld voor de bouw. </br>
De Heidemaatschappij heeft de opdracht voor de aanleg toegewezen gekregen.</br>
Start is gepland maart 1991. </br>
</br>
De opening van de  baan werd op 22 september met o.a. een extra wedstrijd gevierd.</br>
Clubgebouw: vergunning werd op 18 februari verstrekt. De start vond plaats op 1 maart. </br>
Hans de Wijs en Gerrit Hoekman trokken de kar en dat in samenspraak met architect en gemeente. </br>
Een bedrag van fl. 250000 werd gereserveerd. Al wat de club aan werkzaamheden deed kon van het bedrag worden afgetrokken en ten faveure voor de DJA worden gebruikt.</br>
Voor inrichting en materialen werden ook sponsoren geworven. De middenstand was bereid een eenmalig bedrag te sponsoren even als </br>
de plaatsing van 14 bordsponsoren. In totaal kon een bedrag van fl. 7000 de kas invloeien. Later werd nog een sponsorloop georganiseerd.</br>
Ook de oud papier actie en de grote clubactie brachten geld in het laatje.</br>
</br>
Een feestdag door bestuur georganiseerd,  op dezelfde zin als vorig jaar. Barbecue en disco. Het was de beloning voor alle inspanningen.</br>
Bij al dit feestgebeuren werd ook nog een triest afscheid gemeld. Men moest afscheid nemen van Angela Els,t een van de fanatieke leden van DJA.</br>
Sportman en sportvrouw 1991: Jan Schrauwen en Karin Kamphorst. </br>
TC brengt 2 personen naar voren, waarna het bestuur beslistte. </br>
Op 2 november werd een extra ALV gehouden deze keer in de eigen kantine.</br>
