<h3> Algemeen </h3>

Samenstelling van het bestuur was als volgt: </br>
J. v. Hees (voorz.a.i., TC, WBAC), D. v. Bergen (secr., TC, trainersverg.), Elly de Labie (penningm.), Annie van Baal (lid), </br>
Els de Wijs (sinds 01.09.88). In oktober kwam daar Hans de Wijs bij.</br>
Er zit een nieuwe baan voor DJA in de planning bij de gemeente. Een kleine commissie bereidde dit voor. </br>
Deze bestond uit: Jac van Nispen (trainer), G. Hoekman (TC) en Ria van Kempen (RSC) en het bestuur.</br>
Vanaf september 1988 werden de gesprekken tussen DJA en B&amp;W van Zundert over een nieuwe accommodatie hervat.</br>
Er werd een Eisen- en Wensenpakket tot de realisering van een kunststof atletiekbaan door  DJA opgesteld. </br>
Dit werd in de vorm van een nota genaamd ”EEN NOODKREET”  aan de gemeente aangeboden. </br>
Daarbij sprak DJA haar voorkeur uit om deze baan op de Klein Zundertseweg te realiseren. Maar fam. de Bie wilde de daarvoor noodzakelijke grond niet verkopen.</br>
Alternatief  was de locatie bij de “Moerse Boys”. </br>
Een jaar later wilde De Bie toch verkopen maar de gemeente heeft toch een sterke voorkeur voor de locatie nabij de  “Moerse Boys”. </br>
Hetgeen ook de uiteindelijke keuze werd. </br>
Besprekingen  met gemeente en KNAU werden namens DJA gevoerd door Gerrit Hoekman, Hans de Wijs en Elly de Labie. </br>
Deze gesprekken gingen  over de accommodatie en niet over de locatie. Na de  raadsvergadering zou dan in juni 1990 gestart worden met dit project.</br>
</br>
Gerrit Hoekman werd tot ERELID benoemd.</br>
Oud papier draaide o.l.v. Hoekman en Verheyen prima.</br>
Verkoop van 1000 loten Grote Clubactie was weer een goed resultaat. </br>
Jaarlijkse feestavond: 26 maart (disco, maar minder geslaagd voor ouderen). Toch maar weer Rojoki´s. ´s Middags was er weer bowlen bij de Oude Bareel.</br>
Eind 1988  kreeg Ron Schijfs een formeel bedankje voor al zijn werk dat hij voor DJA had gedaan.</br>
Sportman en sportvrouw 1988: John van Sprundel en Nancy Roovers.</br>
Bij de Sport Stichting Zundert werden de WBAC kampioenen Monique Elst (cross), Wendy Tuytelaars (verspringen) en Katrien van Hees (600 m) gehuldigd.</br>
Het aantal leden bedroeg: Atletiek 123 en recreatie 118.  </br>
