<h3>Algemeen </h3>

<b>1982 (DJA 20 jaar!) </b></br>
 In dit jaar vonden gesprekken plaats om de gymvereniging Zundert over te nemen en onder te brengen bij DJA.</br>
De eisen van DJA op de waarborging van de financiën waren onhaalbaar. Dus ging het niet door. </br>
Extra financiën werden nog steeds gevonden in de verkoop van oud papier en clubloten. </br> 
Daardoor was de aanschaf van een eigen geluidsinstallatie mogelijk. </br>
De schoolatletiek die voor de basisscholen bedoeld was werd nu uitgebreid met de middelbare school. Ook werd de accommodatie door diverse scholen gebruikt. </br>
</br>
Het bestuur bestond uit de volgende personen: J. Kimmel,voorzitter, Jan Hereijgers, secretaris,</br> 
M. Jansen, penningmeester en afgevaardigde bij de Sportraad.</br>
Leden: Mevr. C. Konings en A. Michielsen (RSC), A. Hereijgers en C. Vissers (atletiek, WBAC).</br>
In de loop van het jaar nam Thea van Nispen de functie van J. Boomaars (na 6½jr) over als wedstrijd secretraris (uit-wedstrijden). </br>
A. Hereijgers (thuis-wedstrijden).</br>
Begin van het jaar: 219 leden, waarvan 120 recreatie en 99 atleten.</br>
</br>
  

