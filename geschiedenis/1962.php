<p>In de jaren vijftig waren enkele Zundertenaren lid van de atletiek club DJA (inderdaad de Jonge Athleet) maar dan uit Princenhage.<br> 
	Zundert had in die tijd geen ”eigen” atletiek club.<br>
	DJA Princenhage was in feite een atletiek vereniging voor de jongens uit de omliggende dorpen van Breda. <br>
	Een prominent lid was Jan Adriaensen uit Rijsbergen. Hij was een top atleet op de cross en lange afstand.<br>
	Naast hem waren ook lid Gerrit Hoekman, Jan van Gaal, Wim Bastianen en Jac Donkers.<br><br>

Bij de toen al bestaande vereniging Sprint “pasten” die jongens niet in de stadse vereniging.<br>
In 1957 kwam een einde aan het bestaan van DJA (Princenhage) door een terugval in het aantal leden<br>
die voor alle kosten moesten opdraaien (herhaalt de geschiedenis zich?).<br><br>

Op initiatief van Jan van Gaal werd er op 15 juni 1962 in café De Hoek een oprichtings vergadering gehouden<br>
voor een Zundertse atletiek vereniging. <br>
Het voorlopige bestuur werd gevormd door: Jan Gaal vz Jos van Aert secr. Louis de Bruyn penn. Leden Wim Herijgers, Cees Vissers en Guust van Hassel. <br>
De naam was snel gevonden men nam “gewoon” de naam uit Princenhage over. DJA Zundert was geboren. <br>
De contributie werd vastgesteld op 1,50 gulden voor 19 jaar en ouder. Voor de jongeren en militairen 1 gulden. <br>
De training zou plaats vinden op zondag morgen met als trainer Jan van Gaal  voor sprint- en technische nummers, Jan Adriaansen voor de lange afstand.<br><br>

Op 24 augustus 1962 werd de eerste  algemene leden vergadering gehouden. <br>
Het was in die tijd gebruikelijk om de opening en de sluiting van vergadering te doen  met een Christelijke groet. <br>
De vergadering koos het volgende bestuur, waarbij tevens werd bepaalt dat de vergadering de voorzitter kiest,<br>
 de overige functies werden onderling binnen de gekozenen verdeeld.<br>
Voorzitter Piet Hamel, Penningmeester Louis de Bruijn, Secretaris Jos van Aert en leden Jan Adriaensen en Cees Vissers. <br>
De thuishaven werd Sportpark De Wildert, met als atletiekbaan een sintelbaan aangelegd tussen twee voetbalvelden.<br>
Het aantal leden bedroeg 34 (senioren) en de eerste jeugdleden, de broers Peter en Gerard Donkers. <br>
Aan materiaal was beschikbaar enkele kogels, een discus (meegenomen uit Princenhage) en een aantal speren. <br>
Polsstokhoog springen gebeurde als volgt: De gemeente bracht een vrachtwagen met zand dat werd weggekieperd. <br>
Hierboven werd een touw gehangen en de eerste polsstok was een steigerpijp.<br><br>

Op 1-11-1962 werd DJA geregistreerd bij de KNAU met als oprichtingsdatum 12-06-1962. <br>
De leden konden nu aan wedstrijden meedoen. De Warande loop te Tilburg was de eerste wedstrijd na de registratie, <br>
daaraan werd o.a. Cees Vissers 5e Janus Hereijgers 23e en Karel van de Wouw 28e aan deelgenomen. Zij zijn nog steeds lid van DJA.</p>

