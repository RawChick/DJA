<h3>Trim club</h3>
De trim club vierde in  maart 1976 haar 5-jarig bestaan. Op kosten van DJA werd er gebowld in Roosendaal. <br>
Dit uitje beviel zo goed dat men dit ook het volgende jaar wilde doen en dat gecombineerd met het carnavalsbal. <br>
DJA zal in 1977 haar 15 jarig bestaan vieren, ter bekostiging daarvan werd een Kerstkaartenactie gehouden.<br>
De trim club was zeer actief want ook de volgende evenementen werden door hen georganiseerd: <br>
oriëntatietocht, een lentetocht met 125 deelnemers en dan het hoogtepunt (voor de vierde keer) de vierdaagse op 2, 3, 4 en 5 augustus met 600 deelnemers. <br>
Men vertrok vanaf het sportpark voor de afstanden: 5-10-15 km.<br>
<br>

<h3>Jeugd</h3>
Voor de jeugd werd een jeugdmiddag georganiseerd, die een aantal leuke spellen bevatte.<br>
Het was niet alleen feesten voor hen maar ter bekostiging van de viering van 15 jarig bestaan van DJA werd de jeugd aan het werk gezet.<br> 
Zij probeerden 500 sets kerstkaarten te verkopen voor 2 gulden per set, waarvan 0,25 gulden voor de verkoper.<br>
<br>
Op initiatief van Jan Hereijgers kwam men tot oprichting van een jeugdcommissie. <br>
Deze bestond uit voorzitter Jan Hereijgers (ook namens het bestuur) en verder uit elke jeugdcategorie iemand: <br>
Saskia Hoekman, Toos van Opdorp, Hans Gommers, Elvina Monden, Kees van Aert, Martin Radder (wordt later: Eric Snoek en Jac van Tichelt), <br>
Edoo van Aert en Corina Monden.<br>
<br>
Ronny Schijfs werd door het meest behaalde aantal punten atleet van het jaar. <br>
Naast deze commissie ontstond ook de commissie nevenactiviteiten, de bezetting hiervan staat niet op schrift.<br>
<br>
<h3>Recordcommissie</h3>
Vervolgens werd ingesteld een record commissie die bestond uit J. Hereijgers, J. van Aert en G. Hoekman. <br>
Zij ontwierpen een formulier waarop “naar eer en geweten” de prestaties konden worden genoteerd. <br>
Men verlangde wel een ondertekening van een begeleider of jurylid.<br>
Bijzonder is toch wel dat de toen gevestigde records nu nog steeds in de boeken staan.<br> 
<br>
Hier die records t/m 1976 gevestigd en tot nu niet verbetert:<br>
Oudste record bij de senioren Jac van Aert op de 5000 meter 15.000.6 min (1969)<br>
Bij de C junioren Ron Schijfs op de 100 meter 12.3 sec en speerwerpen 600gr 38.00 (in 1975). <br>
Bij de D junioren de 4x 80 meter in 47.0 sec met André Frijters, Peter Frijters, Jaco Hoekman en Cees van Aert. <br>
In deze categorie sprong André Frijters ook nog 5.05 meter ver  (in 1976), <br>
later zullen de nog bestaande records gevestigd in de daarop volgende jaren aan bod komen.<br>
<br>

<h3>Atleten</h3>
De atleten hadden de reguliere kalender en de deelname aan die wedstrijden lag op ongeveer 35%. <br>
Aan wedstrijden buiten de DJA namen in totaal over het jaar 60 atleten mee. <br>
Nieuw was wel de sponsoring door Wim van Oosterhout, die 10 atleten een trainingspak in DJA kleuren bezorgde. <br>
Deze trainingspakken werden wel eigendom van DJA<br>
De nieuwe opzet van het clubcross kampioenschap werd gerealiseerd met een deelname van 2 pupillen, <br>
30 junioren en slechts 8 senioren was men voor de laatste categorie zeker niet tevreden.<br>
Een seniorteam nam deel aan de competitie 4e klas KNAU met wedstrijden in Venlo, Tilburg en Valkenswaard.<br>
<br>
Het gezegde dat de geschiedenis zich herhaald wordt bevestigd door de oproep om vooral toch de clubkleding bij wedstrijden te dragen.<br>
<br>
