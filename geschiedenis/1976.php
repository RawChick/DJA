<h3>Trainingen</h3>
In het trainerscorps zijn ook een aantal wijzigingen zichtbaar, <br>
we zien Jef Maanders, Cees Vissers, Johan en Annie van Aert, Gerrit Hoekman, J. de Bruijn (van VV Zundert). <br>
Eind 1976 komt W. van Uythoven erbij. Deze laatste als vervanger van Jef  Maanders, <br>
waarmee het bestuur een verschil van inzicht in de trainingsopbouw had. <br>
Bij het sollicitatiegesprek waren aanwezig: J. Schrauwen (vz), A. Hereijgers (secr), J. van Aert (trimleider), J. Hereijgers (bestuurslid).<br>
