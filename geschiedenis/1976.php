						<p><h3>Algemeen</h3>
We beginnen deze terugblik met de constatering dat het bestuur uit de volgende personen bestaat: <br>
Jan Schrauwen (vz), J. Kimmel (penningmeester en interimvoorz., Schrauwen gestopt, gaat verhuizen in 1976), A.I.M. Hereijgers (secr), <br>
leden: J. Hereijgers, J. van Aert, G. Hoekman, C.Vissers, A. Michielsen (ipv L. Kuypers) en A. Hombergen-Rens.<br>
Het blijkt dat men toch weer tijdig het aantal bestuursleden op peil weet te houden want vacatures worden bijna opvolgend ingevuld. <br>
Het bestuur vergaderde altijd op de derde dinsdag van de maand bij een van de bestuursleden thuis.<br>
Het aantal leden groeide naar 64 atleten en 50 trimmers.<br>
Cees Vissers werd wederom sportman van het jaar. Jac Ooms werd clubkampioen door bij de 10 kamp de meeste punten te behalen.<br>
