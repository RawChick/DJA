De atleten schrijven zich voortaan zelfstandig in.</br>
</br>
Georganiseerde wedstrijden: DJA clubkampioenschappen, Indoor wedstrijd, regionaal kampioenschap C/D jun (hierbij haalde Lieke Buiks een 3e  plaats). </br>
Het aantal deelnemers bij de wedstrijden liet te wensen over. Clubcross 22 december: bijzonder slecht weer, van de 74 opgegeven deelnemers deden uiteindelijk 48 mee. </br>
