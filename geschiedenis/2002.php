<h2>12-06: 40 jaar DJA</h2>
</br>

<h3>Algemeen</h3>
Samenstelling: Jan Dictus (vz.), Harry van Hasselt (secr.), Thom Schrijnemakers (pennm.), Helen Vermeiren (lid), Bert Grit (lid), Johan Mathijssen. </br>
De laatste werd in september gevraagd en trad toe tot het bestuur.</br>
Het aantal leden is gegroeid naar 453. Het wachtlijstsysteem voor de jeugd bestaat nog steeds. </br>
</br>
Deze is gebaseerd op de ruimte die beschikbaar is in sporthal Onder de Mast.</br>
Het jaar 2002 is te beschouwen als het feestjaar. Dit om de viering van het 40 jarig bestaan luister bij te zetten. </br>
Ieder evenement door DJA georganiseerd kreeg een feestelijk tintje. Hoogtepunten:  de dauwloop op 9 juni 52 deelnemers starten om 04.30 uur hun rondjes. </br>
De inschrijfgelden en sponsorgelden waren voor kinderboerderij Amarant en Speel O’theek. Op de feestavond op 12 juni kon een bedrag van € 735 worden verdeeld. </br>
Het 4-koppige ensemble “t Rottoir” trad op. </br>
</br>
De RSC organiseerde een wandeltocht in het kader van de hartstichting. 142 Deelnemers en een opbrengst van € 330,00.  </br>
De kantineverbouwing van de Moerse Boys gaf hen de gelegenheid om de DJA kantine te huren.  </br>
Diana van Bergen is nog steeds contactpersoon scholen en Jan Dictus voor de Moerse Boys. </br>
De RoPaRunners (Stichting Running Team Zundert) behaalde een 42e plaats van de 182 teams. Afscheid genomen van: Marian Oostvogels (leden administratie).</br>
</br>
Er is een aanvang gemaakt met de opzet van een website door Harry van Hasselt, Marlies van Geel en Wil van Beek.</br>
Sportvrouw van het jaar gemeente Zundert: Gerda Nouws. </br>
In 2001 werd voor het eerst het predicaat “meest verdienstelijk lid” ingesteld. </br>
Johan van Aert viel de eer te beurt deze erkenning als eerste te mogen ontvangen. </br>
Dit jaar werden 2 personen meest verdienstelijk lid: Jan van Hees en Marlies van Geel.</br>
Atleet van het jaar: David Bömer.</br>
Recreant van het jaar: Truus Roovers.</br>
Junior van het jaar: Anita Oostvogels voor de 4e maal. Anita is nu senior geworden.</br>
</br>
<h3>Trainingen</h3>
Eindelijk is de zoektocht naar een technische trainer ten einde. Met dank aan Maria Verdaasdonk en Evie Roelands kan hun interne taak worden </br>
overgenomen door Cees Matthijssen die ook de C/D jun bijstaat.</br>
We hebben afscheid moeten nemen van Bennie Wirix als looptrainer i.v.m. drukke werkzaamheden.</br>
Ook Petra Boot, die de gymlessen verzorgt, werd aangesteld.</br>
Kirsten Snepvangers en Linda Oostvogels hebben het certificaat “Assistent jeugdleider” behaald. </br>
De vervolgmodule "Lopen" werd door Anouk Vermeiren, Linda van de Velde en Maria Verdaasdonk gevolgd.</br>
In oktober 2002 werd een obesitas groep door Ann Smout en Marlies van Geel getraind.   </br>
</br>
<h3>RSC</h3>
RSC-commissie: Ans Kerstens (vz.), Ria van Kempen (pen.m.), Nel Bosma en Jeanne Braspennincx (RSL), Wil van Beek, </br>
Karien Hereijgers en Diana van Opdorp (leden). Afgev.bestuur: Helen Vermeiren. Wil van Beek en  Nel Bosma traden af.</br>
In dit jaar werd de 30e vierdaagse gehouden met als startplaats accommodatie DJA. Om meer deelnemers te krijgen werd de 15 km in het programma opgenomen.</br>
Jolanda Gommers is tot de RSC toegetreden.  </br>
</br>
