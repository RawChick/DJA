<p>DJA was alweer toe aan haar derde voorzitter in zes jaar tijd. Cees Vissers was de durfal.</br>
Na de dip in 1967 trok het aantal leden nu weer aan. Naast dit feit kon men nog twee historische gebeurtenissen noteren.</br>
Per mei kreeg DJA subsidie op haar trainingskosten van de gemeente. Het tweede feit was dat het eerste clubblad het levenslicht zag. </br>
Het bestond uit een blanco kartonnen omslag met daarin 4 x A5 jes op doorslag carbonpapier. </br>
Men was in staat om dit blad 7 maal in dat jaar te laten verschijnen.</br>
</br>
De inhoudelijke hoofdpunten zal een zekere herkenning op leveren namelijk:</br>
<ul>
<li>Openingswoord van de voorzitter</li>
<li>Wedstrijdprogramma</li>
<li>Uitslagen</li>
<li>“D.J.A.”- tjes</li>
<li>Trainer aan het woord.</li>
</ul>

Niet verassend is dat de voorzitter in zijn voorwoord blij was met het verschijnen van het blad, </br>
daarbij de hoop uit sprekend dat deze mag uitgroeien naar een volwaardig blad en dat de leden voor voldoende kopij zullen zorgen.</br> 
Uit het wedstrijd programma ziet men dat de atletiek een sport van traditie is, </br>
immers in het voorjaar stonden de crossen gepland die in de zomer plaatsmaakten voor baanwedstrijden, </br>
in de herfst schakelde men over op de wegwedstrijden die dan in de winter weer overgingen in crossen. </br>
Verder beperkte men zich niet tot de regio maar ook wedstrijden in België en buiten de provincie stonden vermeld. </br>
Ook bij de uitslagen komt een bekend fenomeen tevoorschijn namelijk veel meer jeugd- dan senioren uitslagen.</br></br>

Bij D.J.A. tjes werd een voorval beschreven, dat “iemand” in zijn ijver om tijdig bij de cross te komen zijn trainingspak bij een hindernis scheurde. </br>
Boze tongen beweerden dat hij de scheur opliep omdat hij achterna werd gezeten door een eend, daarbij was de eend toch sneller.</br></br>
De trainer geeft een uitleg over de klasse indeling. Daaruit valt op te maken dat de klassenindeling begint bij D junioren met een leeftijd van 12 of 13 jaar. </br>
Zoals een trainer betaamt doet hij in alle edities een oproep tot meer volgen van trainingen en het leveren van arbeid daarin. </br></br>
In tegenstelling tot 1967 werd nu als clubkampioenschap voor junioren een 5-kamp gehouden,</br> 
voor de senioren bleef de tienkamp gehandhaafd met als kampioen A. Boumans.</br>
Wat opviel in de uitslag was, dat geen van de deelnemers zich waagde  aan de polsstokhoog sprong. </br>
Ook speerwerpen werd niet door een ieder uitgevoerd, toch maakten beide onderdelen uit van de tienkamp. </br>
De winnaar, sloeg beide onderdelen over maar desondanks wist hij een punten aantal van 2992 te verzamelen.</br>
Vermeldenswaard is, dat men naar bepaalde wedstrijden gezamenlijk met een bus reisde, waarbij de club bijdroeg in de reiskosten. </br></br>
Tot slot eindigde men dat jaar met de scholierencross op tweede Kerstdag.</br>
Met een begroting die 1717 bedroeg werd het jaar afgesloten</br></p>
		
