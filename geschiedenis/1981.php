<h3>Algemeen</h3> 

Het jaar 1981 kenmerkte zich door een lastige  financiële situatie. <br>
De financiële toestand was zo nijpend dat het plan om donateurs te werven weer om de hoek kwam kijken. <br>
Dat leidde er toe dat het bestuursavondje niet door ging. Ook al omdat een feestdag op 28 maart op de planning stond. <br>
Deze om het 10 jarig bestaan van de recreatiesport te vieren. Ook de adverteerders kregen een uitnodiging voor deze dag.<br>
Willem Froeling, fysiotherapeut, gaf de groep trainers uitleg over blessurepreventie. <br><br>
Een nieuw element zag het levenslicht: vergaderingen met trainers en recreatiesportleiders. <br>
Daarbij werd ook een trainingscoördinator ingesteld, Jan Hereijgers nam deze taak opzich. <br>
Een 20-tal mensen nam deel aan het schuttersfeest van het 300-jarige gilde St.Willibrordus te Klein Zundert.<br>
Men had behoefte aan een geluidsinstallatie en een bijdrage aan de penningmeester werd dan ook gevraagd.<br><br>
Het bestuur bestond uit: <br>
J. Kimmel,voorzitter, J. Hereijgers, secretaris, M. Jansen, penningmeester,(en lid van Sportstichting Zundert), <br>
G. Hoekman (TC) en A. Michielsen (RSC). Daarbij nog mevr. C. Konings en J. v. Aert (RSC), C. Vissers en A. Hereijgers (TC).<br>
Sinds maart 1981 heeft Jan Hereijgers zitting in de Unieraad van de KNAU.<br>

