<p><h3>Algemeen</h3>

In 1979 kwam een nieuwe wet verenigingsrecht tot stand. Dat had tot gevolg dat de statuten moesten worden aangepast. </br>
Op de algemene ledenvergadering waren niet genoeg leden aanwezig om dat te realiseren. Er moest een extra vergadering worden uitgeschreven.</br></br>
Het volgende bestuur was toen in functie: Voorz. J. Kimmel, secretaris/penningmeester A. Hereijgers, leden: </br>
G. Hoekman (WBAC+publikatie Weekblad), C. Vissers, A. Rens, J. van Aert en A. Michielsen. </br>
Controle administrateur.: dhr. A.v. Dorst, wedstrijdsecretaris J. Boomaars, financiële administratie J. Hereijgers.</br>
Kascommissie: J. Schrauwen en C. Zwaard en mevr. R. Michielsen-Dictus.</br></br>
Jan Hereijgers deed het voorstel om het clubblad te bekostigen uit de opbrengsten van advertenties.</br>
In 1979 is er een Sportraad Zundert in het leven geroepen vanuit een initiatief in de gemeenteraad. </br></br>
Secretaris. A.v. Keulen, voorzitster M. Nierich-Bekking (dhr.v.Giessel), wnd. voorzitter. Fr. Korsmit .</br>
Deze sportraad werd het overkoepelend orgaan van de sportbeleving in Zundert. </br></br>
De verplichte sportkeuring die nodig was om lid te zijn van de KNAU werd van Breda nu ook door de huisartsen in Zundert gedaan. In totaal 54 leden.</br>
In dit jaar werd voor het eerst een materialenlijst opgesteld.</br>
</br>
<h3>Trainingen</h3>
Fien Hereijgers en Johan van Aert slaagden voor het examen recreatiesportleiders terwijl Thea Oostvogels dat al een jaar daarvoor deed.</br>
RSC: voor het eerst op woensdagochtend uurtje gymzaal Turnstraat 9-10 uur.</br>
Sporthal 18-20 uur op woensdagvond en 18-19 uur op vrijdagavond, vanaf winterseizoen 1979. </br>
Trimgroep gaat soms op donderdagavond fietsen. (laatste 4 donderdagavonden voor trimstop).</br>
Het trimuur op het RBT veilingterrein wordt op 19 april daar beëindigd.</br></br>
Aantal leden veranderde als volgt van 112 recreanten, 92 atletiek, totaal: 204 leden  naar 123 recreanten en 90 atleten, totaal 213 leden.</br></br>
De atleten konden op zondag, dinsdag, woensdag en donderdag trainen.Op vrijdag was er de mogelijkheid, om na overleg met de trainer, </br>
individuele training op gewenste onderdelen te krijgen. Ook regelde Cees Vissers dan extra conditie trainingen.</br>
</br>
<h3>Trimclub vanaf heden RSC</h3>
We kunnen dus spreken over de RSC die nu als volgt is samengesteld:</br>
Fons van Dijck stopt als voorzitter en wordt vervangen door Dré Michielsen.</br>
Leden: F. Hereijgers, J. Zagers, J. v. Aert, A. v. Aert, A. Rens, A. Michielsen, J. v.d. Zanden, F. v. Dijck, R. v. Beek.</br>
C. Konings zorgde voor het eerst voor trimfrontnieuws in het clubblad. Hij had het o.a. over</br>
Bingo- bal- feestavonden en bowlingmiddag,  fotopuzzel wandeltocht, sport-spelavond, strandloop in Wassenaar.</br>
De avondvierdaagse brak haar record met 625 deelnemers.</br></br></br>
Als nieuw feit werden coopertesten gehouden in samenwerking met de recreatiegroep uit Sprundel, men begroette 47 deelnemers, waarvan 20 uit Sprundel.</br> 
Daarnaast werd ook nog een voetbalwedstrijd tegen Sprundel georganiseerd.</br>
</br>
<h3>Jeugd</h3>
Ina Frijters (Gommers) deed verslag van de deelname aan de Zuid Nederlandse kampioenschappen voor C junioren. Jammer genoeg geen podium plaatsen. </br>
Ook deed ze een oproep om meer deelnemers.</br></br>
Corina Monden was bij de WBAC kampioenschappen de klapper met overwinningen op de 100m en bij het verspringen. </br>
Andre Frijters behaalde ook nog een podiumplaats: 3e bij het verspringen.</br>
</br>
<h3>Atleten</h3>
Voor de Technische Commissie (TC) stond, voor het eerst in het DJA-bestaan, ook de organisatie van baanwedstrijden in Zundert op het programma</br>
Commissie start nu in de volgende samenstelling:</br>
G. Hoekman, C. Vissers, J. Boomaars, M. Bömer, J. Ooms en A. Hereijgers.</br></br>
Als eerste grote test gold het WBAC kampioenschap voor BCD junioren.</br>
Bijzonder was toch ook dat men het onderdeel kogelslingeren later in het jaar organiseerde.</br></br>
Dankzij dhr. A. Voermans werd een kogelslingerplaats gecreëerd op een weiland aan de Eldertstraat.</br>
Frans Zagers gaf toestemming om voor wintertrainingen en crossen gebruik te maken van een bosperceel.</br>
Aan de NK indoor namen deel : A. Frijters, P. Frijters en R. Schijfs. </br>
Ook aan de Zuid Nederlandse kampioenschappen namen DJA leden deel:  R. Schijfs, A. Frijters, W. Voermans en W. van Uythoven</br></br>
Bij de NK behaalden de atleten W. v. Uythoven en Ron Schijfs respectievelijk </br>
de volgende resultaten kogelslingeren: 1e (52.46 m) en 5e bij het hinkstapspringen (14.34 m).</br></br>
De lange afstandsloper Louis de Hoon waagde zich aan de marathon in Zwolle.</br>
Cor Zwaard nam deel aan een 25 km wedstrijd, kennelijk gingen de afstanden langer worden naar mate DJA langer bestond.</br></br>
Bij de competitie senioren kwam de ploeg in de finale en werd daar  9e met 6732 punten. </br>
De finale werd bereikt door bij de  voorwedstrijden  een 1e plaats en een 2e plaats te  bemachtigen. </br>
Er deden respectievelijk 16 en 11 ploegen mee. De punten aantallen logen er ook niet om 6889 en 6874.</br></br>
Als resume DJA was aanwezig bij 84 wedstrijden met in totaal 767 deelnemers die aan 1367 nummers meededen.</br>
Sportman/vrouw van het jaar W. van Uythoven en Corina Monden.</br>
