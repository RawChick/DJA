<h3>Algemeen</h3> 

Dit jaar vierde DJA haar 25 jarig bestaan. Alle evenementen kregen een tintje in de richting van dat bestaan. <br>
Om gelden te verkrijgen werd een sponsorloop georganiseerd. Deze bestond uit het zoveel mogelijk rondjes afleggen binnen een half- c.q een uur. <br>
De opbrengst was fl. 5700,00<br>
De viering werd opgeluisterd met een receptie, koffietafel en feestavond. Deze werd muzikaal opgeluisterd door de Rojoki’s.<br>
<br>
Het bestuur had de volgende samenstelling:<br>
J. v. Hees (voorz+WBAC+Sportstichting+TC), Diana van Bergen (secr.+TC+ trainersverg.), Elly de Labie (penn.), <br>
Annie van Baal en F. Toonen. Fien Toonen stapt er begin 1988 uit.<br>
Louis Tuytelaar ontviel en werd tijdens de ALV herdacht en bedankt.<br>
De procedure voor verkiezing van sportman/vrouw was wederom aan wijziging onderhevig.<br>
De TC diende daartoe een voorstel in bij het bestuur.<br>
Verkozen in 1987: Roy Hoogaerts en Jolanda Dictus.<br>
Oud papier en publicaties via Gerrit Hoekman (sinds eind 1986 ook op DJA-terrein papier in te leveren).<br>
900 loten Grote Clubactie.<br>
<br>
228 leden (incl. jury- en ereleden). Atletiek: 99 leden, recreatie:114 leden (96 dames en 18 heren).<br>
<br>
    

