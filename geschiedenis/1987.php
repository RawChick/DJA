<h3>Atleten </h3>

Begin 1987 bestond de TC uit 11 leden: Diana van Bergen (voorz.+bestuur), Thea van Nispen (secr.uit-wedstr.), Ron Schijfs (secr. thuiswedstr.), <br>
Berlinde Verhagen (jury), Jan Snepvangers (EHBO, records, vergaderzaken), Gerrit Hoekman (wedstr.organisatie, materialen, publiciteit), <br>
Jan van Hees (bestuur, WBAC), Jac Verheijen (accomm. + wedstr.terrein),  John en Peter van Sprundel (div.).<br>
Jac van Nispen stopt met ingang van 1987. In zijn plaats komt A. Aerts.<br>
<br>
Op 5 april werd de eerste indoorwedstrijd georganiseerd. Alleen toegankelijk voor eigen atleten. (Sporthal Onder de Mast).<br>
De open Junioren ABCD-wedstrijd kende een deelnemersveld van 97 atleten.<br>
De Jubileumwedstijd (een pupillenwedstrijd) telde slechts 68 atleetjes. <br>
<br>
Dit jaar is regeling van inschrijving voor wedstrijden gewijzigd: alle atleten krijgen gratis 6 wedstrijden, dit met een max. bedrag van fl. 7,50 per wedstrijd <br>
(indien wedstrijd meer kost moet men zelf bijbetalen). Bijbetalingen moeten altijd vóór de inschrijvingsdatum gebeuren.<br>
Thuiswedstrijden blijven gratis. <br>
De regel fl.1,-- per wedstrijd betalen gaat dus per 01.04.1987 niet meer op.  <br>
