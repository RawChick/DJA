<h3>RSC </h3>

Annie van Baal (best.+voorz.+RS-leidster), Karel Wamsteker (recr.), Ans Kerstens en Nel Bosma (RS-leidster), W. Kustermans, Ineke Fransen, Ria van Kempen.<br>
