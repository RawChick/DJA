Verder sloot DJA zich dit jaar aan bij de WBAC (West Brabantse Atletiek Commissie). <br>
De 1e WBAC-cross  werd gehouden bij Spado in Bergen op Zoom. Daar presteerden de A-Pupillen uitzonderlijk goed. <br>
