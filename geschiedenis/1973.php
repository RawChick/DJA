Dit jaar was ook het jaar waarin de trimclub zich formeel in DJA voegde. Ook werden de leden nu aan de KNAU opgegeven.<br>
Dat betekende dat het aantal leden van DJA in één klap met 60 leden groeide. Toch liep het bij de trimmers niet naar wens, <br>
