<h3>Recordcommissie </h3>

In november 1983 werd het voorstel ingediend  tot het  aanpassen clubrecords: </br>
Deze zouden dan in de volgende categorieën worden ingedeeld: </br>
-officiële </br>
-officieuze </br>
-incourant </br>
-indoor. </br>
</br>
Deze indeling bleek een succes. Daarnaast werd bepaald dat de records binnen 3 maanden na de geleverde prestatie(s) </br>
ter erkenning aan de recordcommissie moeten worden voorgelegd.</br>
Jac van Nispen vestigde een record op de 3 engelse mijlen (incourant) met 16.36.6 min. Deze tijd staat nog steeds</br>. 
