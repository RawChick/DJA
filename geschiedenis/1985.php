<h3>Algemeen </h3>

Op 26 maart 1985 werd een extra algemene ledenvergadering gehouden. Dit omdat men een voorstel tot statutenwijziging wilde behandelen. <br>
Het is niet opgetekend wat de inhoud van de statutenwijziging inhield. Er waren inclusief bestuur 43 leden aanwezig.<br>
G. Hoekman nam de organisatie van de oud papier actie over van de gebroeders Hereijgers.<br>
DJA hoopt in 1987 haar 25 jarig bestaan te vieren, reeds nu werd een activiteiten commissie in het leven geroepen.<br>
 J. Hereijgers en C.Vissers, waren reeds lid van verdienste. ERELIDMAATSCHAP nu.<br>
<br>
M. Jansen, wist als penningmeester het financiëel jaarverslag tot goed einde te brengen<br>
Daarin stond o.a. dat bij de grote clubactie 800 loten werden verkocht en dat je donateur kon worden door een bedrag van fl.10,00 te storten.<br>
Het voltallig bestuur zag zich genoodzaakt om in haar geheel af te treden. <br>
<br>
Nieuw bestuur werd gevormd met als a.i. voorzitter Jan van Hees. Leden: Annie van Baal (RSC), <br>
Diana van Bergen (secr.), mevr. F. Toonen en dhr. J. Goossens (penn).<br>
Wederom ontstond het gevaar dat er veel werk voor enkelen voor de boeg lag.<br>
J. Goossens ging kort na de benoeming verhuizen, de functie werd overgenomen door Elly de Labie.<br>
<br>
Op de reguliere jaarvergadering in februari werden J. Hereijgers en C. Vissers benoemd als lid van verdienste.<br>
Totaal eind jaar: 181 leden. Ledental daalde met 22 in totaal. 103 Leden recreatie, waarvan 15 mannen en 88 vrouwen. <br>
Totale verloop van dit jaar is 53. Daarbij viel het grote verloop onder de atletiekleden op.<br>
<br>
<h3>Trainingen </h3>

Ron Schijfs is in de loop van 1985 opgevolgd door Ad Aerts die de junioren jongens onder zijn hoede nam.<br> 
Jac van Nispen (o.l.v. Jef Maanders) begint met het geven van looptrainingen.<br>
Bij recreatiesport is Fien Hereijgers gestopt als trainster.<br>
<br>
<h3>RSC </h3>

De RSC commissie bestond uit: Annie van Baal (best.lid), Johan van Aert (RSC-lid), <br>
Jeanet Mathijssen later de Leeuw-Mathijssen (RSC-lid). Daar kwamen bij: E. Roelands, K. Wamsteker, Elly de Labie en N. Meulenbroek. <br>
Deze laatste kwam algauw tot de ontdekking dat de functie niet bij haar paste en trad weer af. <br>
In mei 1985 zijn er drie RSC-leiders bijgekomen: Ans Kerstens, Nel Bosma en Esther van Baal.<br>
<br>
De al eerder in vorige terugblikken genoemde evenementen stonden ook dit jaar weer op het programma.<br>
Het vorige jaar ging het langlaufen door gebrek aan belangstelling niet door, nu ging men er met een volle bus heen<br>
Vermeldenswaard is de viering van de 65e verjaardag van het oudste DJA-lid Netje Meulenbroeck.<br>
DJA was vertegenwoordigd met 30 leden bij het inhalen van de Canadese rolstoel-atleet Rick Hansen die bij Wernhout Nederland binnenkwam.<br>
