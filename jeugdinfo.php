<h3>Jeugdtrainingen</h3>

Voor de Mini-Pupillen tot en met de C-Junioren (7 t/m 15 jaar) wordt er twee keer per week één uur training gegeven.</br>
In het winterseizoen traint de jeugd in sporthal 'Onder de Mast' in Zundert, op de accommodatie en/of in het bos 'de Krochten' in Wernhout.</br>
In het zomerseizoen wordt er twee keer per week op de DJA accommodatie aan de Akkermolenweg getraind.</br>
Klik <a href="trainingstijdenjeugd.php">hier</a> voor meer info over de trainingstijden.</br>
</br>
De B-Junioren trainen twee keer per week 1 tot 1½ uur. Zowel in het zomer- als winterseizoen traint deze groep op de accommodatie, </br>
waar zij werken aan hun conditie, kracht en techniek.</br>
</br>
<h3>Doelstelling</h3>

DJA streeft er naar haar jeugdleden een zodanige opleiding of begeleiding te geven, dat zij technische eiindvormen van alle atletiekonderdelen </br>beheersen. Hierbij hoort ook een technische loopvaardigheid en conditionele inhoud.</br>
Dit alles wordt op een speels-prestatieve manier aangeboden, waarbij het plezier in de sport steeds voorop staat. </br>
De jeugdtrainers werken volgens een bepaald opleidingsplan waardoor een verantwoorde doorstroom gewaarborgd is.</br>
</br>

<h3>Jeugdwedstrijden</h3>

Deelname aan wedstrijden wordt gestimuleerd, maar is niet verplicht.</br>
Competitiewedstrijden, ARWB-kampioenschappen en thuiswedstrijden zijn altijd onder begeleiding van de train(st)ers. </br>
Bij andere (open) wedstrijden kunnen de train(st)ers aanwezig zijn.</br>
Cilia Mathijssen verzorgt het wedstrijdsecretariaat uit voor de pupillen en de C/D-junioren.</br>
</br>
Wedstrijden in het zomerseizoen:

Pupillen: twee competitiewedstrijden op de atletiekbaan, gevolgd door een eventuele finale.</br>
Junioren: drie competitiewedstrijden op de atletiekbaan, gevolgd door een eventuele finale.</br>
ARWB-kampioenschappen voor zowel pupillen als C/D-junioren.</br>
De interregiowedstrijd. Hiervoor worden de tijdens competitiewedstrijden, kampioenschappen en andere vooraf aangegeven wedstrijden, </br>
best presterende pupillen en C/D-junioren geselecteerd.</br>
Wedstrijden in het winterseizoen:</br>
</br>
Vijf ARWB-crossen in het bos (competitiewedstrijd).</br>
Verder zijn er in de zomer nog een aantal open baanwedstrijden waaraan men kan deelnemen en in de winter zijn er de indoorwedstrijden.</br>
Per seizoen wordt een wedstrijdkalender gemaakt zodat men tijdig op de hoogte is.</br>
