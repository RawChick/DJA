<h3>Recreant - lopers</h3>

Naast de wedstrijdtrainingen kan men binnen onze vereniging ook op diverse manieren aan recreatiesport doen, o.a. trimmen of joggen. <br>
Of zoals wij gewoon zeggen: lopen in eigen tempo.<br>
Iedereen die een beetje sportief is kan aan deze vorm van recreatiesport meedoen. Onder deskundige begeleiding, <br>
waarbij gezelligheid voorop staat, kun je toch een lekker "endje weglopen", zowel met mannen als met vrouwen.<br>
De leeftijd loopt uiteen van ± 25 jaar tot ruim 60 jaar.<br>
Er wordt een gezamenlijke warming-up en cooling-down gedaan om verantwoord bezig te zijn.<br>
<br>
Op donderdagavond wordt er op de accommodatie getraind. Op dinsdagavond zijn we 's winters op de weg te vinden, en 's zomers in het bos.<br>
Op zaterdagmiddag is er een extra training voor de langere-afstandlopers. Ook is er een groep op donderdagochtend actief.<br>
<br>
Regelmatig wordt er door diverse recreanten deelgenomen aan trimloopwedstrijden, zoals de Van Goghloop in Zundert, <br>
de Bavoloop in Rijsbergen en de Stratenloop in Hoogstraten. In de winter wordt er aan crossen deelgenomen. <br>
Het ontspannende aspect blijft wel voorop staan!<br>
<br>
<h3>Recreanten</h3>

Deze afdeling bestaat uit een aantal groepen. Er wordt niet zozeer gelopen, maar er wordt op een gezonde manier aan de conditie gewerkt. <br>
