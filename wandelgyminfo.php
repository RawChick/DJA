<b>Dinsdagmiddag wandelen</b></br>
</br>
Bij de dinsdagmiddag-groep gaan we er van uit dat iedereen mee moet kunnen wandelen. Het tempo wordt aan de groep aangepast.</br>
Er wordt elke week een andere route gekozen.</br>
Deze wandelgroep telt momenteel ongeveer 21 leden, variërend in de leeftijd van 30 tot achter in de zestig jaar.</br>
</br>
We vertrekken iedere maand vanaf een andere locatie waar men op eigen gelegeheid naar toe kan komen.</br>
