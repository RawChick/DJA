<h3>Gymgroep onder leiding van José Lambregts</h3>

Op woensdagochtend wordt er tussen 09.00 uur en 10.00 uur gym gegeven in sporthal "Onder de Mast".</br>
</br>
Men begint de les met een warming-up d.m.v. allerlei loopoefeningen in de zaal. Ieder doet dit op haar eigen tempo. </br>
Daarna volgen spierversterkende oefeningen. Ook hier geldt: alles mag, niets moet. Dit is i.v.m. de beperkingen die sommigen hebben.</br> 
Bij de één is dat de schouder, bij de ander een knie of iets anders.</br>
</br>
We sluiten de les af met een spel, waarin iedereen zijn overgebleven energie kwijt kan. </br>
Daarna volgen nog wat rek- en strekoefeningen om spierpijn te voorkomen.</br>
</br>
De groep bestaat uit dames in de leeftijd van ± 30 jaar tot ver in de 70 jaar en staat onder leiding van José Lambregts-Ooms, tel. 076-5973153.</br>
